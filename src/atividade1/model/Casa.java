package atividade1.model;

public class Casa {
    public final String endereco;
    public final int quantidadeDeQuartos;
    public final int numero;
    public final double areaDoTerreno;

    public Casa(String endereco, int quantidadeDeQuartos, int numero, double areaDoTerreno) {
        this.endereco = endereco;
        this.quantidadeDeQuartos = quantidadeDeQuartos;
        this.numero = numero;
        this.areaDoTerreno = areaDoTerreno;
    }

}
